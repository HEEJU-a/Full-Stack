package sec06.ch03;

public class LogicalOperatorExam {
  public static void main(String[] args) {

		//&&(and), ||(or), !(not)
		
		boolean res1 = (1>7) && true && false;
	    System.out.println("res1 : " + res1);
	    
	    boolean res2 = (10>2) || false || true;
	    System.out.println("res2 : " + res2);
	    
	 
	
	}
	  
 
}
